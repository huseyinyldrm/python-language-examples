
'''Veri Tiplerini ogrenmek icin type() fonksiyonu kullanilir.'''

a=3.5
print(type(a)) # float

b=9
print(type(b)) # intager

c="hello"
print(type(c)) # string

d=(4>5)
print(type(d)) # bool

e=(3,4,5,'a','+') # tuple
print(type(e))

f=[1,2,3,'%','a'] # list
print(type(f)) 

g=[1,2,3,4,5] # list class hepsi ayni veri tipi olunca
