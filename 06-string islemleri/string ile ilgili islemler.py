str="Hüseyin"
print(str) # doğrudan yazdırır
print(str[3:]) # 3. indisten başlar sonuna kadar gider.
print(str[::2]) #baştan başlar ve ikişerli atlar.
print(len(str)) # verilen stringin uzunluğunu verir.
print(str +" DİYARBAKIR") # ekleme yapar.
print(str.upper()) # tüm harfleri büyük yapar.
print(str.lower()) # tüm harfleri küçük yapar.
print(str.split()) # verilen ifadeyi stringden listeye dönüştürür.
print(str.split(sep='i',maxsplit=2)) # i den ayırı ve 2 karakterlik boşluk bırakır.
print(str[::-1]) # ifadeyi ters yazdırır.
print(str.capitalize()) # ilk harfi büyük yazdırır.
print(str.swapcase()) # küçük harfi büyük ,büyük harfi küçük yazdırır.

