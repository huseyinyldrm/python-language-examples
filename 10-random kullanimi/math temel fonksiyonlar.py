import math

print(abs(-5)) # mutlak değerdir.
print(round(2.6)) # 2.5 ten büyükse 3'e değilse 2'ye yuvarlar.
print(pow(3,2)) # 3'ün 2 üssünü alır.
print(bin(10)) # içindeki değeri binariye çevirir.
print(hex(21)) # içindeki değeri hekzadesimale çevirir.
print(chr(66)) # aski koduna çevirir. makine dilindeki karşılığı.
print(math.pi) # pi sayisini yazdirir.
print(math.sqrt(9)) # karekök alır.
print(math.gcd(81,27))# en buyuk boleni yazdirir.
